/**
 * Get translation for a key in the current language
 */
function t(key) {
    const lang = getCurrentLanguage();
    if (translations[lang] && translations[lang][key]) {
        return translations[lang][key];
    }
    // Fallback to English
    if (translations.en && translations.en[key]) {
        return translations.en[key];
    }
    // Return key itself if not found
    return key;
}

/**
 * Get current language from localStorage or browser default
 */
function getCurrentLanguage() {
    try {
        const saved = localStorage.getItem('appLanguage');
        if (saved && translations[saved]) {
            return saved;
        }
    } catch (e) {
        console.warn('Could not read language preference:', e);
    }
    // Default to English
    return 'en';
}

/**
 * Set language and apply to entire UI
 */
function setLanguage(langCode) {
    if (!translations[langCode]) {
        console.warn('Language not supported:', langCode);
        return false;
    }
    
    try {
        localStorage.setItem('appLanguage', langCode);
    } catch (e) {
        console.warn('Could not save language preference:', e);
    }
    
    // Apply translations to UI
    applyTranslations();
    
    return true;
}

/**
 * Get available languages
 */
function getAvailableLanguages() {
    return Object.keys(translations).map(code => ({
        code,
        name: translations[code].lang_name
    }));
}

/**
 * Apply translations to all elements with data-i18n attribute
 */
function applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const translated = t(key);
        if (el.tagName === 'INPUT') {
            el.placeholder = translated;
        } else {
            el.textContent = translated;
        }
    });
}

// Auto-apply translations when page loads
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', applyTranslations);
} else {
    applyTranslations();
}

// Export for modules
if (typeof module !== 'undefined' && module.exports) {
    module.exports = {
        translations,
        t,
        getCurrentLanguage,
        setLanguage,
        getAvailableLanguages,
        applyTranslations
    };
}
